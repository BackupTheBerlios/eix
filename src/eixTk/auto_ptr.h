#ifndef __AUTO_PTR_H__
#define __AUTO_PTR_H__

namespace eix {

	template<typename _Type>
	class simple_array_auto_ptr
	{

		public:
			simple_array_auto_ptr(_Type *p)
				: m_p(p)
			{ }

			~simple_array_auto_ptr()
			{ 
				if(m_p)
					delete[] m_p; 
			}

			_Type* get() const
			{ return m_p; }

		protected:
			_Type* m_p;
	};

}

#endif /* __AUTO_PTR_H__ */
